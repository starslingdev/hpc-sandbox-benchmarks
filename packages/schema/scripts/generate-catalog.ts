#!/usr/bin/env bun
// `generate-catalog` — regenerate `src/pts-generated.ts` from every vendored profile under
// `src/pts-profiles/`. Reads the committed XML (never the network), maps each profile to draft
// `MetricDef[]` (./catalog/generate.ts), serializes deterministically (./catalog/serialize.ts), and
// pipes the result through Biome so the committed bytes match a fresh run — the invariant the catalog
// drift gate enforces. Run by hand after vendoring/bumping a profile (`bun run generate-catalog`);
// nothing imports it at build time.
import { readdir } from "node:fs/promises";
import { generateCatalog } from "./catalog/generate.ts";
import { parseProfile } from "./catalog/parse.ts";
import { serializeCatalog } from "./catalog/serialize.ts";

const PROFILES_DIR = `${import.meta.dir}/../src/pts-profiles`;
const OUT_FILE = `${import.meta.dir}/../src/pts-generated.ts`;

async function readXml(path: string): Promise<string> {
	const file = Bun.file(path);
	return (await file.exists()) ? file.text() : "";
}

/** Regenerate `src/pts-generated.ts` in place. Exported so the drift gate can re-run it directly. */
export async function generateCatalogFile(): Promise<void> {
	// Sort the profile dirs so generation order — and thus output — is independent of readdir order.
	const dirs = (await readdir(PROFILES_DIR, { withFileTypes: true }))
		.filter((d) => d.isDirectory())
		.map((d) => d.name)
		.sort();

	const profiles = await Promise.all(
		dirs.map(async (dir) =>
			parseProfile(
				dir,
				await readXml(`${PROFILES_DIR}/${dir}/test-definition.xml`),
				await readXml(`${PROFILES_DIR}/${dir}/results-definition.xml`),
			),
		),
	);

	const catalog = generateCatalog(profiles);
	await Bun.write(OUT_FILE, serializeCatalog(catalog));

	// Format in place with the repo's pinned Biome so re-generated bytes are byte-stable (drift gate).
	const fmt = Bun.spawn(["bunx", "biome", "format", "--write", OUT_FILE], {
		stdout: "inherit",
		stderr: "inherit",
	});
	if ((await fmt.exited) !== 0) throw new Error("biome format failed on generated catalog");

	console.log(`✓ ${catalog.length} metrics from ${dirs.length} profiles -> src/pts-generated.ts`);
}

if (import.meta.main) {
	try {
		await generateCatalogFile();
	} catch (err) {
		// Log the whole error (preserves the stack) so a failure is debuggable, not just its message.
		console.error("generate-catalog failed:", err);
		process.exit(1);
	}
}
