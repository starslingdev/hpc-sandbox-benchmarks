import { describe, expect, test } from "bun:test";
import type { MetricDef } from "../../src/metrics.ts";
import { serializeCatalog } from "./serialize.ts";

const a: MetricDef = {
	id: "a_metric",
	dimension: "cpu",
	unit: "runs/s",
	direction: "HIB",
	headline: false,
	label: "A",
	description: "first",
	pts: { test: "pts/a" },
};
const b: MetricDef = {
	id: "b_metric",
	dimension: "disk",
	unit: "MB/s",
	direction: "HIB",
	headline: false,
	label: "B",
	description: "second",
	pts: { test: "pts/b", description: "Mode: X" },
	sourceUrl: "https://example.test/b",
};

describe("serializeCatalog", () => {
	test("is a byte-reproducible, order-preserving formatter", () => {
		const ab = serializeCatalog([a, b]);
		// Same input → identical bytes (the drift gate diffs these).
		expect(serializeCatalog([a, b])).toBe(ab);
		// serialize does NOT reorder — the stable id sort is generateCatalog's job, so a reversed
		// input yields reversed output. (Order-independence is asserted on generateCatalog instead.)
		expect(serializeCatalog([b, a])).not.toBe(ab);
		expect(ab.indexOf('"a_metric"')).toBeLessThan(ab.indexOf('"b_metric"'));
	});

	test("emits a typed, importable ptsGenerated export", () => {
		const src = serializeCatalog([a]);
		expect(src).toContain('import type { MetricDef } from "./metrics.ts";');
		expect(src).toContain("export const ptsGenerated: MetricDef[] = [");
		expect(src).toContain("AUTOGENERATED");
	});

	test("serializes object keys in canonical order, pts before sourceUrl", () => {
		const src = serializeCatalog([b]);
		const order = [
			"id",
			"dimension",
			"unit",
			"direction",
			"headline",
			"label",
			"description",
			"pts",
			"sourceUrl",
		].map((k) => src.indexOf(`${k}:`));
		expect(order).toEqual([...order].sort((x, y) => x - y));
	});

	test("omits pts.description for a wildcard entry, keeps it otherwise", () => {
		expect(serializeCatalog([a])).toContain('pts: { test: "pts/a" }');
		expect(serializeCatalog([b])).toContain('pts: { test: "pts/b", description: "Mode: X" }');
	});

	test("chunks past the literal ceiling and spreads every chunk back, in order", () => {
		// The multi-chunk path had zero coverage: CHUNK_SIZE is 250 and every fixture here holds ≤2
		// entries, so no test ever produced a chunk2 — and the drift gate structurally cannot catch a
		// boundary bug, since it compares the committed bytes against a fresh run of the SAME
		// serializer. 251 entries is one past the boundary, where an off-by-one drops an entry.
		const defs: MetricDef[] = Array.from({ length: 251 }, (_, i) => ({
			...a,
			id: `m_${String(i).padStart(3, "0")}`,
		}));
		const src = serializeCatalog(defs);
		expect(src).toContain("const chunk1: MetricDef[] = [");
		expect(src).toContain("const chunk2: MetricDef[] = [");
		expect(src).not.toContain("chunk3");
		expect(src).toContain("export const ptsGenerated: MetricDef[] = [...chunk1, ...chunk2];");
		// Every entry survives exactly once, in input order.
		expect(src.match(/\bid: "m_\d{3}"/g)).toHaveLength(251);
		expect(src.indexOf('"m_249"')).toBeLessThan(src.indexOf('"m_250"'));
	});
});
